// embed-fonts.mjs — base64-embed the webfonts into assets/fonts.css as @font-face data URIs,
// so styling+fonts work with zero external/relative fetches (localhost, file://, or sandboxed preview).
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const FILES = (pkg) => join(ROOT, "node_modules", "@fontsource", pkg, "files");

const RANGE = {
  latin: "U+0000-00FF,U+0131,U+0152-0153,U+2000-206F,U+2074,U+20AC,U+2122,U+2212,U+FFFD",
  thai: "U+0E01-0E5B,U+200C-200D,U+25CC",
};

// [family, cssFamily, weight, [subsets]]
const SPECS = [
  ["noto-sans-thai", "Noto Sans Thai", 400, ["latin", "thai"]],
  ["noto-sans-thai", "Noto Sans Thai", 700, ["latin", "thai"]],
  ["noto-sans-thai-looped", "Noto Sans Thai Looped", 400, ["latin", "thai"]],
  ["noto-sans-thai-looped", "Noto Sans Thai Looped", 600, ["latin", "thai"]],
  ["ibm-plex-mono", "IBM Plex Mono", 400, ["latin"]],
  ["ibm-plex-mono", "IBM Plex Mono", 500, ["latin"]],
];

let css = "/* Embedded webfonts (base64 woff2) — generated by tools/embed-fonts.mjs */\n";
let total = 0;
for (const [pkg, family, weight, subsets] of SPECS) {
  for (const sub of subsets) {
    const file = join(FILES(pkg), `${pkg}-${sub}-${weight}-normal.woff2`);
    const b64 = readFileSync(file).toString("base64");
    total += b64.length;
    css += `@font-face{font-family:"${family}";font-style:normal;font-weight:${weight};font-display:swap;` +
      `src:url(data:font/woff2;base64,${b64}) format("woff2");unicode-range:${RANGE[sub]};}\n`;
  }
}
writeFileSync(join(ROOT, "assets", "fonts.css"), css);
console.log(`Embedded ${SPECS.length} weights -> assets/fonts.css (~${Math.round(total / 1024)} KB base64)`);
